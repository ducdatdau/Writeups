X = [0x24, 0x27, 0x13, 0xC6, 0xC6, 0x13, 0x16, 0xE6, 0x47, 0xF5, 0x26, 0x96, 0x47, 0x0F5, 0x46, 0x27, 0x13, 0x26, 0x26, 0xC6, 0x56, 0xF5, 0xC3, 0xC3, 0xF5, 0xE3, 0xE3, 0x0]

# 0xab * 16 = 0xab << 4 = 0xab0 
# 0xab >> 4 = 0xa 
# (0xab << 4) | (0xab >> 4) = 0xaba 

flag = 'DH{'

for i in X:
    # a = (i & 0x0F) 
    # b = int((i & 0xF0) / 16)
    # ab = a * 16 + b 

    a = (i & 0x0F) << 4
    b = (i & 0xF0) >> 4 
    ab = a | b
    flag += chr(ab) 

flag += '}'

print(flag)

